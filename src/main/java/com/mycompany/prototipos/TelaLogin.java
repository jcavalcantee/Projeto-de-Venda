    private void validacaoLogin() { //Metódo para verificar qual usuário fez login na aplicação
        String idGa = "1015";
        String idJe = "1017";
        String idLu = "1019";
        String idPa = "1021";
        String idPe = "1023";
        String login = txtID.getText();
        
        if(login.equals(idGa)) {
            JOptionPane.showMessageDialog(rootPane, "Bem-Vindo, Gabriel!", "Confirmação de Login", HEIGHT);
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            this.dispose();
        } else if (login.equals(idJe)) {
            JOptionPane.showMessageDialog(rootPane, "Bem-Vindo, Jefferson!", "Confirmação de Login", HEIGHT);
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            this.dispose();
        } else if (login.equals(idLu)) {
            JOptionPane.showMessageDialog(rootPane, "Bem-Vindo, Lucas!", "Confirmação de Login", HEIGHT);
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            this.dispose();
        } else if (login.equals(idPa)) {
            JOptionPane.showMessageDialog(rootPane, "Bem-Vindo, Patrick!", "Confirmação de Login", HEIGHT);
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            this.dispose();
        } else if (login.equals(idPe)) {
            JOptionPane.showMessageDialog(rootPane, "Bem-Vindo, Pedro!", "Confirmação de Login", HEIGHT);
            TelaPrincipal tela = new TelaPrincipal();
            tela.setVisible(true);
            this.dispose();
        } else 
            JOptionPane.showMessageDialog(rootPane, "ID Inválido. Tente Novamente", "Login Negado", HEIGHT);
    }
    private void btnEntrarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnEntrarKeyPressed
        char t = evt.getKeyChar();
        
        if(t == KeyEvent.VK_ENTER) {
            validacaoLogin();
        } else
            JOptionPane.showMessageDialog(rootPane, "ID Inválido");
    }//GEN-LAST:event_btnEntrarKeyPressed

